package lectures.part1basics

object VariablesClass extends App{
  //vals are immutable
  val x: Int = 42
  println(x)
  val aString: String = "Hello"
  println(aString)
  val aBoolean: Boolean = true
  println(aBoolean)
  val aChar: Char = 'a'
  println(aChar)
  val anInt: Int = 1
  println(anInt)
  val aShort: Short = 345
  val aLong: Long = 34556666L // 8 Bytes
  val aFloat: Float = 2.0f
  println(aFloat)
  val aDouble: Double = 3.14

  // vars are mutable
  var y: Int = 6


}
